
public class ThreadStat {
	
	private long thread_num;
	private int stat;
	
	public ThreadStat(){
		thread_num = 0;
		stat = 0;
	}

	public long getThread_num() {
		return thread_num;
	}

	public void setThread_num(long thread) {
		this.thread_num = thread;
	}

	public int getStat() {
		return stat;
	}

	public void setStat(int stat) {
		this.stat = stat;
	}
	
	

}
